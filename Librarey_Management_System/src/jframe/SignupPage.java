    //validation
    public boolean validateSignup() {

    String name = txt_username.getText().trim();
    String pwd = new String(txt_password.getPassword());
    String email = txt_email.getText().trim();
    String contact = txt_telephone.getText().trim();

    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "SVP entrer votre nom !");
        return false;
    }

    if (pwd.isEmpty()) {
        JOptionPane.showMessageDialog(this, "SVP entrer votre mot de passe !");
        return false;
    }

    if (email.isEmpty() || !email.matches("^.+@.+\\..+$")) {
        JOptionPane.showMessageDialog(this, "Email invalide !");
        return false;
    }

    if (contact.isEmpty()) {
        JOptionPane.showMessageDialog(this, "SVP entrer votre telephone !");
        return false;
    }

    return true;
}



