    private void rSMaterialButtonCircle2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonCircle2ActionPerformed
        if (validateSignup()) {
        String password = new String(txt_password.getPassword()).trim();

    User user = new User(
        txt_username.getText().trim(),
        txt_email.getText().trim(),
        password,
        txt_telephone.getText().trim()
    );

    UserDAO userDAO = new UserDAO();

    if (userDAO.isEmailExists(user.getEmail())) {
        JOptionPane.showMessageDialog(this, "Email existe déjà !");
    } else {
        if (userDAO.insertUser(user)) {
            JOptionPane.showMessageDialog(this, "Inscription réussie ");
            LoginPage loginPage = new LoginPage();
            loginPage.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Erreur d'inscription ");
        }
    }
}

        
        
    }//GEN-LAST:event_rSMaterialButtonCircle2ActionPerformed

